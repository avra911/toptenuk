class MagenThemes_MTColinusAdmin_Block_ProductsScroller extends Mage_Catalog_Block_Product_List
{
    public function getListProducts()
    {
    	$products = null;
    	$mode=$this->getConfig('mode');
	switch ($mode) {
	    case 'latest' :
		$products = $this->getListLatestProducts();
		break;
	    case 'bestseller' : 
		$products = $this->getListBestSellerProducts();
		break;
	    case 'mostviewed' :
		$products = $this->getListMostViewedProducts();
		break;
	    case 'featured' :
		$products = $this->getListFeaturedProducts();
		break;
	    case 'new' :
		$products = $this->getListNewProducts();
		break;
	}
	
	return $products;
    }
    
    public function getListLatestProducts($fieldorder = 'updated_at', $order = 'desc')
    {
    	$storeId    = Mage::app()->getStore()->getId();
    	$cateids = $this->getConfig('catsid');
    	if($cateids) {
    	    $arr_productids = $this->getProductByCategory();
	    $products = Mage::getResourceModel('catalog/product_collection')
		    ->addAttributeToSelect('*')
		    ->addMinimalPrice()
		    ->addUrlRewrite()
		    ->addTaxPercents()
		    ->addStoreFilter()
		    ->addIdFilter($arr_productids)
		    ->setOrder ($fieldorder,$order);
    	} else {
	    $products = Mage::getResourceModel('catalog/product_collection')
		    ->addAttributeToSelect('*')
		    ->addMinimalPrice()
		    ->addFinalPrice()
		    ->addStoreFilter()
		    ->addUrlRewrite()
		    ->addTaxPercents()
		    ->setOrder ($fieldorder,$order);
    	}		
        Mage::getSingleton('catalog/product_status')->addVisibleFilterToCollection($products);
        Mage::getSingleton('catalog/product_visibility')->addVisibleInCatalogFilterToCollection($products);
        $products->setPageSize($this->getConfig('qty'))->setCurPage(1);
        $this->setProductCollection($products);
	return $products;
    }
    
